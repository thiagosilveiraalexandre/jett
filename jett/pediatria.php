            <h3>Infectologia pediátrica.</h3>
            <p>A infectologia Pediátrica é uma subespecialidade pediátrica que atende as crianças e adolescentes com doenças infecciosas. A Infectologia Pediátrica atua junto ao pediatra titular do caso, de forma integrada e com o objetivo de contribuir para esclarecimento diagnóstico e/ou para orientação terapêutica.Qual é a função do infectologista?
            O médico infectologista é um profissional treinado para realizar o diagnóstico, tratamento e prevenção das doenças infecciosas e parasitárias. Após anos de estudos e sólida formação clínica, tornam-se especialistas nas infecções causadas por vírus, bactérias, fungos e parasitas.</p>

            <h3>Nefrologia pediátrica.</h3>
            <p>Nefrologia é uma especialidade médica dedicada ao diagnóstico e tratamento clínico das doenças do sistema urinário, principalmente relacionadas ao rim. O médico especializado nas doenças do sistema urinário chama-se médico nefrologista A maneira mais eficaz para identificar precocemente as doenças renais é por meio de exames de sangue e urina. A dosagem da creatinina sanguínea permite calcular a taxa de filtração sanguínea dos rins.</p>
            
            <h3>Nutrologia pediátrica</h3>
            <p>Nutrologia Pediátrica é uma especialidade médica voltada para a Nutrição e suas diversas interfaces na criança, desde doenças (gastro, alergia, nefrologia), prevenção com orientação de alimentação saudável e tratamento de carências nutricionais, dificuldades alimentares, erros alimentares, obesidade entre outros.O nutrólogo pediátrico pode realizar a melhor orientação e detectar possíveis carências nutricionais que potencialmente levam a prejuízo do crescimento e desenvolvimento cerebral/retiniano.</p>
