            <h3>Check-up rotineiro</h3>
            <p>Quando o paciente sente dores na garganta e/ou no ouvido, é recomendado um checkup rotineiro para determinar a causa do problema e possívelmente prescrever remédios para tratar a condição.</p>

            <h3>Septoplastia</h3>
            <p>Consiste na abordagem cirúrgica do septo nasal para a correção de desvios nas porções cartilaginosa e óssea, para a melhora da potência nasal. É realizada por dentro do nariz, podendo ser necessário o auxílio do endoscópio; O procedimento é realizado pela doutora Glanzner com auxílio de enfermeiros residentes.</p>
            
            <h3>Tonsilectomia</h3>
            <p>Consiste na retirada das amígdalas, quando o paciente está com caso grave de amigdalite, esse procedimento é evitável se o paciente buscar tratamento anterior. Também é realizado pela doutora Glanzner.</p>
