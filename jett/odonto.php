            <h3>Cárie</h3>
            <p>A cárie é uma lesão que se forma no esmalte dentário por causa dos ácidos liberados por bactérias que consomem os resíduos de alimento depositados sobre os dentes. Pode ser muito discreta e não provocar sintomas, mas a degradação do dente é progressiva. Além disso, ele pode ter toda a sua estrutura comprometida, levando à perda do dente. Prevenir a cárie também é muito simples, porque, assim como para outras doenças bucais, basta manter uma boa higiene. É fundamental escovar os dentes após cada refeição, de preferência com um creme dental com flúor, e usar o fio dental pelo menos uma vez ao dia. Evitar o açúcar e carboidratos simples também ajuda a preveni-la.</p>

            <h3>Gengivite</h3>
            <p>É uma inflamação que afeta a gengiva, provocada por bactérias que se instalam em suas bordas em função da higiene bucal inadequada. Esses microrganismos liberam ácidos ao consumirem os resíduos de alimento, e o organismo reage com essa resposta inflamatória. Para que não ocorra, se faz essencial escovar os dentes após cada refeição e usar o fio dental até a borda da gengiva. Mas como essa doença também pode ser causada pelo diabetes, é importante manter a glicose sob controle.</p>
            
            <h3>Placa Bacteriana</h3>
            <p>A placa bacteriana não se trata de uma doença bucal. No entanto, ela é a principal causa da maioria dos problemas que afetam a boca. Trata-se de um biofilme que se forma no esmalte dentário em função do acúmulo de resíduos de comida. Eles servem como alimento para bactérias, e são elas que desequilibram a saúde bucal. A prevenção da placa bacteriana é simples. Basta manter uma rotina de higiene adequada realizando a escovação dos dentes após cada refeição, seguida do uso do fio dental. Além disso, é interessante evitar os alimentos que favorecem sua formação, como açúcar, carboidratos simples, amidos e aqueles com textura pegajosa.</p>
