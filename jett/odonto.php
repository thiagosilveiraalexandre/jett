            <h2>O que é odontologia?</h2>
            <p>A medicina odontológica ou odontologia é a área da saúde que estuda e trata do sistema estomatognático - compreende o crânio, a face, o pescoço e a cavidade bucal, abrangendo ossos, musculatura mastigatória, articulações, dentes e tecidos. Ela atua na limpeza, restauração e extração dos dentes, além de ser responsável por cirurgias, procedimentos estéticos, instalação de próteses e tratatamento de doenças na gengiva, bochecha, língua e ossos da face.
