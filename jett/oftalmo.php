            <h3>Catarata</h3>
            <p>O único tratamento para catarata é o cirúrgico. O objetivo da cirurgia simples, rápida e feita sob anestesia local  é substituir o cristalino danificado por uma lente artificial que recupera a função perdida. Essa lente pode ser de vários tipos e corrigir vários problemas de visão.</p>

            <h3>Miopia</h3>
            <p>O tratamento para miopia pode ser feito com o uso de óculos ou lentes de contato que ajudam a focar os raios de luz, colocando a imagem sobre a retina do olho. No entanto, outra opção é a cirurgia refrativa que pode ser feita, geralmente, quando o grau se encontra estabilizado.</p>
            
            <h3>Astigmatismo</h3>
            <p>O tratamento para astigmatismo passa pela correção do erro refrativo. Qualquer astigmatismo pode ser corrigido através de óculos ou lentes de contato. A correção cirúrgica também é possível, recorrendo a laser (LASIK) ou operar com lentes intra-oculares.</p>
